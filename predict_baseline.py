# ---- Run evaluation ----
def evaluate_map(name, grid_map_data, min_coverage):
    grid_map = np.array(grid_map_data)
    total_spills = int(np.sum(grid_map == 1))
    coverages = []
    attempts = 0

    MAX_ATTEMPTS = 500
    while len(coverages) < NUM_TRIALS and attempts < MAX_ATTEMPTS:
        attempts += 1
        free_cells = np.argwhere(grid_map == 0)
        random_indices = random.sample(range(len(free_cells)), 1)
        robot_positions = np.array([tuple(free_cells[i]) for i in random_indices])

        env = ChemicalClean(grid_map.copy(), num_robots=1,
                            robot_positions=robot_positions,
                            field_of_view=0, chem_fov=1,
                            rewards={
                                "clean_spill": 50,
                                "consecutive_bonus": 150,
                                "revisit_penalty": -10,
                                "explore_penalty": -10,
                                "obstacle_penalty": -5,
                            })

        model = PPO.load(MODEL_PATH, env=env)
        obs = env.reset()
        done = False

        while not done:
            action, _ = model.predict(obs, deterministic=True)
            obs, reward, done, info = env.step(action)

        cleaned = np.sum(env.spill_map == 1)
        coverage = (cleaned / total_spills * 100) if total_spills > 0 else 0

        if coverage >= min_coverage:
            coverages.append(coverage)

    return {
        "mean": np.mean(coverages),
        "std": np.std(coverages),
        "attempts": attempts,
    }

