def obst_poly(obs):
    obs_poly  = []
    for obst in obs:
        obs_poly.append(Polygon(obst))
    return obs_poly






def pygame_rect_to_polygon(rect):
    # Get the coordinates of the corners of the rectangle
    top_left = (rect.left, rect.top)
    top_right = (rect.right, rect.top)
    bottom_right = (rect.right, rect.bottom)
    bottom_left = (rect.left, rect.bottom)
    
    # Create a Polygon from the corners
    polygon = Polygon([top_left, top_right, bottom_right, bottom_left])
    
    return polygon

def rect_to_poly(cells):
    rect_poly = []
    for rect in cells:
        rect_poly.append(pygame_rect_to_polygon(rect))
    return rect_poly



